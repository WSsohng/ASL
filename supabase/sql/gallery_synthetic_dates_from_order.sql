with matched as (
  select
    g.id as gallery_id,
    g.source_present_num,
    g.date_text,
    coalesce(l1.id, l2.id, l3.id) as legacy_id,
    coalesce(l1.has_legacy_date, l2.has_legacy_date, l3.has_legacy_date, false) as has_legacy_date
  from public.gallery_posts g
  left join tmp_gallery_legacy_dates l1
    on l1.source_present_num <> '' and l1.source_present_num = g.source_present_num
  left join tmp_gallery_legacy_dates l2
    on l1.id is null and l2.source_idx <> '' and l2.source_idx = g.source_idx
  left join tmp_gallery_legacy_dates l3
    on l1.id is null and l2.id is null and l3.id = g.id
),
anchor as (
  select coalesce(min(date_text::date), date '2023-12-31') as min_real_date
  from matched
  where has_legacy_date = true
    and date_text ~ '^\d{4}-\d{2}-\d{2}$'
),
target as (
  select
    m.gallery_id,
    m.source_present_num,
    row_number() over (
      order by
        case when m.source_present_num ~ '^[0-9]+$' then m.source_present_num::int else -1 end desc,
        m.gallery_id
    ) as rn
  from matched m
  where m.has_legacy_date = false
    and m.legacy_id is not null
),
updated as (
  update public.gallery_posts g
  set date_text = to_char((a.min_real_date - (t.rn || ' days')::interval)::date, 'YYYY-MM-DD')
  from target t
  cross join anchor a
  where g.id = t.gallery_id
  returning g.id, g.date_text
)
select count(*) as synthetic_updated from updated;
